class MrpcProcessor(DataProcessor):
    """Processor for the MRPC data set (GLUE version)."""

    def get_example_from_tensor_dict(self, tensor_dict: Dict[str, Any]) -> InputExample:
        """See base class."""
        return InputExample(
            tensor_dict["idx"].numpy(),
            tensor_dict["sentence1"].numpy().decode("utf-8"),
            tensor_dict["sentence2"].numpy().decode("utf-8"),
            str(tensor_dict["label"].numpy()),
        )

    def get_train_examples(self, data_dir: str) -> List[InputExample]:
        """See base class."""
        return self.create_examples(self._read_tsv(os.path.join(data_dir, "train.tsv")), "train")

    def get_dev_examples(self, data_dir: str) -> List[InputExample]:
        """See base class."""
        return self.create_examples(self._read_tsv(os.path.join(data_dir, "dev.tsv")), "dev")

    def get_test_examples(self, data_dir: str) -> List[InputExample]:
        """See base class."""
        return self.create_examples(self._read_tsv(os.path.join(data_dir, "test.tsv")), "test")

    def get_labels(self) -> List[str]:
        """See base class."""
        return ["0", "1"]

    @staticmethod
    def create_examples(lines: List[List[str]], set_type: str) -> List[InputExample]:
        """Creates examples for the training, dev and test sets."""
        examples = []
        for (i, line) in enumerate(lines):
            if i == 0:
                continue
            guid = "%s-%s" % (set_type, i)
            text_a = line[3]
            text_b = line[4]
            label = line[0]
            examples.append(InputExample(guid=guid, text_a=text_a, text_b=text_b, label=label))
        return examples


