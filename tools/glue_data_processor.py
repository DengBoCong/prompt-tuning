class TextClassificationProcessor(DataProcessor):
    """ Data processor for text classification datasets (mr, sst-5, subj, trec, cr, mpqa). """

    def __init__(self, task_name: str) -> None:
        self.task_name = task_name

    def get_example_from_tensor_dict(self, tensor_dict: Dict[str, Any]) -> InputExample:
        """See base class."""
        return InputExample(
            tensor_dict["idx"].numpy(),
            tensor_dict["sentence"].numpy().decode("utf-8"),
            None,
            str(tensor_dict["label"].numpy()),
        )

    def get_train_examples(self, data_dir: str) -> List[InputExample]:
        """See base class."""
        return self._create_examples(pd.read_csv(os.path.join(data_dir, "train.csv"),
                                                 header=None).values.tolist(), "train")

    def get_dev_examples(self, data_dir: str) -> List[InputExample]:
        """See base class."""
        return self._create_examples(pd.read_csv(os.path.join(data_dir, "dev.csv"),
                                                 header=None).values.tolist(), "dev")

    def get_test_examples(self, data_dir: str) -> List[InputExample]:
        """See base class."""
        return self._create_examples(pd.read_csv(os.path.join(data_dir, "test.csv"),
                                                 header=None).values.tolist(), "test")

    def get_labels(self) -> List[int]:
        """See base class."""
        if self.task_name == "mr":
            return list(range(2))
        elif self.task_name == "sst-5":
            return list(range(5))
        elif self.task_name == "subj":
            return list(range(2))
        elif self.task_name == "trec":
            return list(range(6))
        elif self.task_name == "cr":
            return list(range(2))
        elif self.task_name == "mpqa":
            return list(range(2))
        else:
            raise Exception("task_name not supported.")

    def _create_examples(self, lines: List[List[str]], set_type: str) -> List[InputExample]:
        """Creates examples for the training, dev and test sets."""
        examples = []
        for (i, line) in enumerate(lines):
            guid = "%s-%s" % (set_type, i)
            if self.task_name == "ag_news":
                examples.append(InputExample(guid=guid, text_a=line[1] + '. ' + line[2],
                                             short_text=line[1] + ".", label=line[0]))
            elif self.task_name == "yelp_review_full":
                examples.append(InputExample(guid=guid, text_a=line[1], short_text=line[1], label=line[0]))
            elif self.task_name == "yahoo_answers":
                text = line[1]
                if not pd.isna(line[2]):
                    text += " " + line[2]
                if not pd.isna(line[3]):
                    text += " " + line[3]
                examples.append(InputExample(guid=guid, text_a=text, short_text=line[1], label=line[0]))
            elif self.task_name in ["mr", "sst-5", "subj", "trec", "cr", "mpqa"]:
                examples.append(InputExample(guid=guid, text_a=line[1], label=line[0]))
            else:
                raise Exception("Task_name not supported.")

        return examples


